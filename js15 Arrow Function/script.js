// More Function

// Normal Function 
function saySomething(name) {
    console.log(`Hello, ${name}`);
}

// Function Expression
let saySomething1 = function(name1) {
    console.log(`Hello, ${name1}`);
}

// Arrow Function (ES6)  >>> Uses in Reat JS, Native 
let saySomething2 = (name2) => {
    console.log(`Hello, I am ${name2}`);
}
saySomething("Shehad")
saySomething1("Uj Jahan");
saySomething2("Imran");